//////////////////////////////////////////////////////////////////////

static inline int abs(int x)
{
    int p = x >> 31;
    return (x ^ p) - p;
}

//////////////////////////////////////////////////////////////////////

static inline int min(int x, int y)
{
    int p = (x - y) >> 31;
    return (x & p) | (y & ~p);
}

//////////////////////////////////////////////////////////////////////

static inline int max(int x, int y)
{
    int p = (y - x) >> 31;
    return (x & p) | (y & ~p);
}

//////////////////////////////////////////////////////////////////////

static inline int clamp(int x, int low, int high)
{
    return min(max(x, low), high);
}
